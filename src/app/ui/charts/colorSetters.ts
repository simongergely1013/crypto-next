export const setBackgroundColor = (condition: boolean) => {
  return condition ? "bg-[#191832]" : "bg-white";
};
export const setChartTextPrimary = (condition: boolean) => {
  return condition ? "text-white" : "text-[#181825]";
};
export const setChartTextSecondary = (condition: boolean) => {
  return condition ? "text-[#D1D1D1]" : "text-[#191932]";
};
